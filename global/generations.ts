export function getAllCollections(): string[] {
    const collections: string[] = [];

    // Loop through each property in the Filters object
    for (const key in Filters) {
        if (Filters.hasOwnProperty(key)) {
            collections.push(...Filters[key].collections);
        }
    }

    return collections;
}

export function getFreeCollections(): string[] {
    const freeCollections: string[] = [];

    // Loop through each property in the Filters object
    for (const key in Filters) {
        if (Filters.hasOwnProperty(key)) {
            const collectionType = Filters[key].type;
            if (collectionType === "free") {
                // Concatenate the "free" collections to the freeCollections array
                freeCollections.push(...Filters[key].collections);
            }
        }
    }

    return freeCollections;
}

export function findCollectionNameByContractAddress(address: string): String {
    for (const collectionName in Filters) {
        if (Object.prototype.hasOwnProperty.call(Filters, collectionName)) {
            const collection = Filters[collectionName];
            if (collection.collections.includes(address)) {
                return collectionName;
            }
        }
    }

    return "new";
}

